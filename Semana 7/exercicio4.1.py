"""

Problema Prático 4.1

Comece executando a atribuição:

s = '0123456789'

Agora, escreva expressões (usando s e o operador de indexação) que sejam avaliadas como:

a) '234'
b) '78'
c) '1234567'
d) '0123'
e) '789'

"""

s = '0123456789'

print(f'a) {s[2:5]}')
print(f'b) {s[7:9]}')
print(f'c) {s[1:8]}')
print(f'd) {s[:4]}')
print(f'e) {s[-3:]}')
